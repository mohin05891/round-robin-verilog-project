`timescale 1ns/1ps

module tb_weighted_rr_arbiter;

reg clk;
reg rst;

reg req0;
reg req1;
reg req2;
reg req3;

wire master0;
wire master1;
wire master2;
wire master3;

//-------------------------------------------
// DUT
//-------------------------------------------
weighted_rr_arbiter uut(
    .clk(clk),
    .rst(rst),
    .req0(req0),
    .req1(req1),
    .req2(req2),
    .req3(req3),
    .master0(master0),
    .master1(master1),
    .master2(master2),
    .master3(master3)
);

//-------------------------------------------
// Clock
//-------------------------------------------
always #5 clk = ~clk;

//-------------------------------------------
// Monitor
//-------------------------------------------
initial
begin
    $dumpfile("weighted_rr.vcd");
    $dumpvars(0,tb_weighted_rr_arbiter);

    $display("--------------------------------------------------------------");
    $display("Time\tReq\t\tGrant");
    $display("--------------------------------------------------------------");

    $monitor("%0t\t%b%b%b%b\t\t%b%b%b%b",
              $time,
              req0,req1,req2,req3,
              master0,master1,master2,master3);
end

//-------------------------------------------
// Test
//-------------------------------------------
initial
begin

    clk = 0;
    rst = 1;

    req0 = 0;
    req1 = 0;
    req2 = 0;
    req3 = 0;

    #20;
    rst = 0;

    //-------------------------------------------------
    // Case 1 : 1111
    //-------------------------------------------------
    $display("\n===== REQUEST = 1111 =====");
    req0=1;
    req1=1;
    req2=1;
    req3=1;

    #120;

    //-------------------------------------------------
    // Case 2 : 1110
    //-------------------------------------------------
    $display("\n===== REQUEST = 1110 =====");
    req0=1;
    req1=0;
    req2=0;
    req3=0;

    #120;

    //-------------------------------------------------
    // Case 3 : 1100
    //-------------------------------------------------
    $display("\n===== REQUEST = 1100 =====");
    req0=1;
    req1=0;
    req2=0;
    req3=0;

    #120;

    //-------------------------------------------------
    // Case 4 : 1000
    //-------------------------------------------------
    $display("\n===== REQUEST = 1000 =====");
    req0=1;
    req1=0;
    req2=0;
    req3=0;

    #120;

    //-------------------------------------------------
    // Back to 1111
    //-------------------------------------------------
    $display("\n===== REQUEST = 1111 =====");
    req0=0;
    req1=0;
    req2=0;
    req3=0;

    #120;

    $finish;

end

endmodule